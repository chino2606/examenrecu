   <center>La suma de polinomios es una operación en álgebra que consiste en combinar dos o más polinomios <br>
    para obtener un nuevo polinomio. <br>
    Este nuevo polinomio se forma sumando los términos semejantes (términos con la misma parte literal y exponente) <br>
    de los polinomios originales. </center>
  <img src="poli.png" alt="logo">

    <table>
        <tr>
            <th>Caracteristicas </th>
        </tr>
        <tr>
            <td>
                Combinación de términos semejantes:
Solo se pueden sumar términos que tienen la misma parte literal (misma variable elevada a la misma potencia). <br>
Suma de coeficientes:
Se suman los coeficientes de los términos semejantes, y la parte literal se mantiene. <br>
Propiedad conmutativa:
El orden en que se suman los polinomios no afecta el resultado (A + B = B + A). <br>
Propiedad asociativa:
La forma de agrupar los polinomios al sumarlos no afecta el resultado ((A + B) + C = A + (B + C)). 
                <br><br>
            </td>
        </tr>
    </table>

    <table>
        <tr>
            <th>Para que sirve</th>
        </tr>
        <tr>
            <td>
               Simplificación de expresiones:
Permite reducir varias expresiones polinómicas a una sola expresión más compacta, lo que facilita su manejo y análisis. <br>
Resolución de ecuaciones:
La suma de polinomios es un paso fundamental en la solución de ecuaciones polinómicas, donde se busca encontrar los valores de las variables que satisfacen la igualdad. <br>
Modelado matemático:
En diversas disciplinas como la física, la ingeniería y la economía, los polinomios se utilizan para modelar fenómenos y relaciones entre variables. La suma de polinomios permite combinar diferentes modelos para obtener uno más completo o representativo de un sistema. <br>
Álgebra abstracta:
En álgebra abstracta, los polinomios son elementos fundamentales para construir estructuras algebraicas más complejas, como anillos y campos. La suma de polinomios es una operación esencial dentro de estas estructuras. <br>
Geometría:
La suma de polinomios puede aplicarse en geometría para definir funciones que describen formas geométricas y sus relaciones. 
