        <li>Ejemplo 1: Suma vertical</li>
        <li>Considera los polinomios: P(x) = 3x² + 5x - 2, Q(x) = x² - 2x + 7. </li>
        <li>Para sumarlos verticalmente, se alinean los términos semejantes y se suman sus coeficientes:</li>
        <li>  3x² + 5x - 2 </li>
        <li>+  x² - 2x + 7</li>
        <li> ---------------- </li>
        <li> 4x² + 3x + 5 </li>
        <li> Ejemplo 2: Suma horizontal</li>
        <li>Considera los polinomios: P(x) = 4x³ - 2x² + 6x - 1, Q(x) = -x³ + 7x² - 3x + 9. </li>
        <li>Para sumarlos horizontalmente, se agrupan los términos semejantes y se suman:  </li>
        <li>P(x) + Q(x) = (4x³ - x³) + (-2x² + 7x²) + (6x - 3x) + (-1 + 9) </li>
        <li>P(x) + Q(x) = 3x³ + 5x² + 3x + 8 </li>
        <li>Ejemplo 3: Suma de polinomios con diferentes grados </li>
        <li>Considera los polinomios: P(x) = 5x⁴ + 2x² - 3x + 1, Q(x) = 3x³ - x + 5.</li>
        <li>En este caso, aunque no haya un término x³ en P(x), se puede incluir un término 0x³ para facilitar la suma: </li>
        <li> 5x⁴ + 0x³ + 2x² - 3x + 1 </li>
        <li>+  0x⁴ + 3x³ + 0x² - x + 5 </li>
        <li>-----------------------------</li>
        <li>5x⁴ + 3x³ + 2x² - 4x + 6 </li>
        <li>La suma de P(x) y Q(x) es 5x⁴ + 3x³ + 2x² - 4x + 6.</li>
        <li> En resumen, la suma de polinomios implica:</li>
        <li>Identificar términos semejantes: Aquellos con la misma variable y exponente. </li>
        <li>Sumar los coeficientes de los términos semejantes.</li>
        <li>Mantener la parte literal. </li>
        <li>Si es necesario, ordenar los polinomios antes de sumar.</li>
