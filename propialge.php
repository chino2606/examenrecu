    <h1>Propiedades Algebraicas de los Polinomios</h1>

    <h2></h2>
    <ul>
        <li> Conmutativa:
El orden de los términos en la suma o multiplicación no altera el resultado. <br>
Por ejemplo, para la suma: P(x) + Q(x) = Q(x) + P(x). Para la multiplicación: P(x) * Q(x) = Q(x) * P(x).</li>
        <li> Asociativa:
En la suma o multiplicación de varios polinomios, la forma en que se agrupan los términos no afecta el resultado. <br>
Por ejemplo, para la suma: (P(x) + Q(x)) + R(x) = P(x) + (Q(x) + R(x)). Para la multiplicación: (P(x) * Q(x)) * R(x) = P(x) * (Q(x) * R(x)).</li>
        <li>Distributiva:
La multiplicación de un polinomio por otro se realiza aplicando la propiedad distributiva, es decir,
 multiplicando cada término del primer polinomio por cada término del segundo.  </li>
        <li> Suma:
El grado del polinomio resultante de la suma de dos polinomios es, a lo sumo, igual al mayor de los grados de los polinomios sumados.</li>
        <li> Multiplicación:
El grado del polinomio resultante de la multiplicación de dos polinomios es igual a la suma de los grados de los polinomios multiplicados. </li>
